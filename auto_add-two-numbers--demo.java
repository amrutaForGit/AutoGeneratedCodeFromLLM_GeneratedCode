// Filename: IntegerAddition.java

public class IntegerAddition {
  
   public static void main(String[] args) {
    
        // Initialize two integers AA and BB 
        int AA = 5;
        int BB = 10;
        
        // Calculate the sum of AA and BB and store it in CC
        int CC = AA + BB;
        
        // Print the result
        System.out.println("The sum of " + AA + " and " + BB + " is: " + CC);
    }
}